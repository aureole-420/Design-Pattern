package model;

import provided.util.ABCInstrument;

/**
 * The model to view adapter that allows model to talk to view.
 * @author Yuhui Tong, Yunxiang Zhang.
 *
 */
public interface IModel2ViewAdapter {
	/**
	 * Display the file content of the .abc file.
	 * @param fileContents The file contents 
	 */
	public void displayFileContent(String fileContents);

	/**
	 * Add instruments to GUI's droplist.
	 * @param instrument The instrument that music is played with.
	 */
	public void addInstrument(ABCInstrument instrument);

}
